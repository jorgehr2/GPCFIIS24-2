const Long MX = 1e6 + 5;
Long n;
vector<Long> v;

bool used[MX];
Long dp[MX];

Long f(Long i) {
    if (i >= n) {
        return 0;
    }
    if (used[i]) {
        return dp[i];
    }
    used[i] = true;

    return dp[i] = max(f(i + 1), v[i] + f(i + 3));
}

